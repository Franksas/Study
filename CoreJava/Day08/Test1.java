/**
使用while进行累加操作，输出1到100累加的和
  @author yinhansong
  @version 1.0
*/
public class Test1{
    public static void main(String [] args){
        int i = 1;
        int sum =0;
        while (i=100){
          sum+=i;
            i++;
          }
          System.out.println(sum);
    }

}
